    private void moveAToB_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_moveAToB_ButtonActionPerformed
        // TODO add your handling code here:
        if (stack.getStackA().isEmpty()) {
            return ;
        }
        
        try {
            for (int i = 0; i < stackSize; i++) {
                System.out.println("Move A to B at i = " + i);
                tableStackB.setValueAt(stack.getStackB().push((stack.getStackA().pop())), i, 0);
                tableStackA.setValueAt(null, i, 0);
            }
            if (!stack.getStackA().isEmpty()) {
                System.out.println("Printing StackA in move A to B");
                showStack(stack.getStackA());
            }
            if (!stack.getStackB().isEmpty()) {
                System.out.println("Printing StackB in move A to B");
                showStack(stack.getStackB());
            }
        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("Error occured");
        }

    }//GEN-LAST:event_moveAToB_ButtonActionPerformed

