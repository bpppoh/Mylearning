    void showStack(Stack<Integer> item) {
        System.out.print("{");
        for (int i = 0; i < stackSize; i++) {
            System.out.print(item.get(i));
            if (i != stackSize - 1) {
                System.out.print(",");
            }
        }
        System.out.println("}");
    }

