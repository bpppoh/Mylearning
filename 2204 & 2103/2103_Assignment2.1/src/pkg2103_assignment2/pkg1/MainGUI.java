    void updateStackA() {
        DefaultTableModel model = (DefaultTableModel) StackATable.getModel();

        model.setRowCount(0);

        for (int i = stackA.maxsize - 1; i >= 0; i--) {
            if (i <= stackA.top) {
                model.addRow(new Object[]{ stackA.stackThing[i] });
            } else {
                model.addRow(new Object[]{ "" });
            }
        }
    }
