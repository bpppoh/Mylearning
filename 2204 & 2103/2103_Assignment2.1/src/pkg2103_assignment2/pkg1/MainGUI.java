    private void CtoBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CtoBActionPerformed
        if (stackC.checkEmpty() || stackB.checkFull()){
            ErrorDialog dialog = new ErrorDialog(this,true) ;
            dialog.setVisible(true);
        } else {
            try{
                System.out.println("Transfer C to B , Start") ;
                stackB.push(stackC.pop());
                System.out.println("Transfer C to B , Success");
                updateStackC() ;
                updateStackB() ;
            } catch (Exception e) {
                System.out.println("Transfer C to B , Failed");
                e.printStackTrace() ;
                ErrorDialog dialog = new ErrorDialog(this,true) ;
                dialog.setVisible(true);
            }
            
        }
    }//GEN-LAST:event_CtoBActionPerformed

