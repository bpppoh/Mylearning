    void InsertAtLast(DNode New) {
        System.out.println("Entering InsertAtLast");
        if (Last != null) {
            DNode LastNode = Last;
            LastNode.Forw = New;
            New.Back = LastNode;
            Last = New;
        } else {
            Start = Last = New;
        }
        System.out.println("Exting InsertAtLast");
        return;
    }

    void InsertAtFirst(DNode New) {
        System.out.println("Entering InsertAtFirst");
        if (Start != null) {
            DNode FirstNode = Start;
            New.Forw = FirstNode;
            FirstNode.Back = New;
            Start = New;
        } else {
            Start = Last = New;
        }
        System.out.println("Exiting InsertAtLast");
    }

    void DeleteByLoc(DNode Loc) {
        System.out.println("Entering DeleteByLoc");
        if (Loc == null || Loc.Back == null || Loc.Forw == null) {
            return; // Safety check
        }
        Loc.Back.Forw = Loc.Forw;
        Loc.Forw.Back = Loc.Back;
        System.out.println("Exiting DeleteByLoc");
        return;
    }

    void DeleteAtFirst() {
        System.out.println("Entering DeleteAtFirst");
        if (Start.Forw != null) {
            Start = Start.Forw;
            Start.Back = null; // Set new Start's Back to null
        } else {
            Start = Last = null;
        }
        System.out.println("Exiting DeleteAtFirst");
    }

    void DeleteAtLast() {
        System.out.println("Entering DeleteAtLast");
        if (Last.Back != null) {
            Last = Last.Back;
            Last.Forw = null; // Set new Last's Forw to null
        } else {
            Start = Last = null;
        }
        System.out.println("Exiting DeleteAtLast");
    }

    DNode Search(int id) {
        System.out.println("Entering Search Method");
        DNode Loc = null;
        DNode Ptr = Start;
        while (Ptr != null) {
            if (id == Ptr.Infor.getID()) {
                System.out.println("Search : Found ! Loc = Ptr");
                Loc = Ptr;
                break;
            }
            System.out.println("Search : Not Found yet. Ptr = Ptr.Forw ");
            Ptr = Ptr.Forw;
        }
        if (Loc != null) {
            System.out.println("Search : Found !");
        } else {
            System.out.println("Search : Not Found !");
        }
        System.out.println("Exiting Search Method");
        return Loc;
    }

