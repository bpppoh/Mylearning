    void displayImage(String imageName) {
        try {
            ImageIcon image = new ImageIcon(getClass().getResource("/image/" + imageName + ".jpg"));
            Image temp_image = image.getImage();

            int FrameWidth = 150;
            int FrameHeight = 150;
            int pictureWidth = image.getIconWidth();
            int pictureHeight = image.getIconHeight();

            double widthRatio = (double) FrameWidth / pictureWidth;
            double heightRatio = (double) FrameHeight / pictureHeight;

            double ratio = Math.min(widthRatio, heightRatio);

            int newWidth = (int) (pictureWidth * ratio);
            int newHeight = (int) (pictureHeight * ratio);

            temp_image = temp_image.getScaledInstance(newWidth, newHeight, Image.SCALE_SMOOTH);

            image = new ImageIcon(temp_image);
            ImageFrame.setIcon(image);
            ImageFrame.setText("");

        } catch (Exception e) {
            ImageFrame.setIcon(null);
            ImageFrame.setText("Image not found");
            System.out.println("Error loading image: " + imageName);
        }
    }

