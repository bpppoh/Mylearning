    private void InsertBehindItemIDActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_InsertBehindItemIDActionPerformed
        // TODO add your handling code here:
        InsertBehindItemIDGUI obj = new InsertBehindItemIDGUI(this, true);
        obj.setVisible(true);
        if (obj.confirm) {
            DNode Ptr = Search(obj.getItemID());
            if (Ptr == null) {
                ErrorDialog dialog = new ErrorDialog(this, true);
                dialog.setVisible(true);
                return;
            }
            try {
                String name = obj.getjTextPane4();
                int type = Integer.parseInt(obj.getjTextPane5());
                float price = Float.parseFloat(obj.getjTextPane6());
                String image = obj.getjTextPane7();

                SportEquipment temp = new SportEquipment(++id, name, type, price, image);
                DNode node = new DNode();
                node.Infor = temp;
                System.out.println("Insert At index " + obj.getjTextPane1() + " is operating . . .");
                
                if (Ptr.Forw == null) {
                    InsertAtLast(node);
                } else {
                    InsertByLoc(Ptr, Ptr.Forw, node);
                }
                
                System.out.println("Insert At Certain index operating is success !");
                amount++;
                updateInfo(node);
                updateTableAtTableGUI();
            } catch (Exception e) {
                System.out.println(e);
                jLabel5.setText("Error");
                jLabel6.setText("-");
                jLabel7.setText("-");
            }
        }
    }// GEN-LAST:event_InsertBehindItemIDActionPerformed

    private void DeleteByIDButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_DeleteByIDButtonActionPerformed
        // TODO add your handling code here:
        DeleteByID dialog = new DeleteByID(this, true);
        dialog.setVisible(true);
        try {
            if (dialog.confirm) {
                System.out.println("Delete At Last is operating . . .");
                // DeleteByIndex(LA, dialog.index, amount);

                DNode item = Search(dialog.id);
                if (item == null) { // Add null check
                    ErrorDialog error = new ErrorDialog(this, true);
                    error.setVisible(true);
                    return;
                }
                if (Start == item) {
                    DeleteAtFirst();
                } else if (Last == item) {
                    DeleteAtLast();
                } else {
                    DeleteByLoc(item);
                }

                System.out.println("Delete At Last operating is success !");
                // index = amount - 1;
                amount--;
                updateInfo(Start);
                updateTableAtTableGUI();
            }
        } catch (Exception e) {
            ErrorDialog error = new ErrorDialog(this, true);
            error.setVisible(true);
            return;
        }
    }// GEN-LAST:event_DeleteByIDButtonActionPerformed

